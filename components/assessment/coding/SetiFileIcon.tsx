/** Returns the seti color for a given extension (used by EXT_ACCENTS elsewhere) */
export const getSetiColor = (ext: string): string => {
    const key = EXT_MAP[ext.toLowerCase()];
    return key ? (ICONS[key]?.color ?? "#6D8086") : "#6D8086";
};

const SetiFileIcon: React.FC<SetiFileIconProps> = ({
    ext,
    filename,
    size = 15,
    className,
}) => {
    // Try filename match first, then extension
    let iconKey = filename ? FILENAME_MAP[filename] : undefined;
    if (!iconKey) {
        iconKey = EXT_MAP[ext.toLowerCase()] ?? "default";
    }
    const icon = ICONS[iconKey] ?? ICONS.default;
    const isDefaultStroke = iconKey === "default";

    return (
        <svg
            width={size}
            height={size}
            viewBox={icon.viewBox ?? "0 0 32 32"}
            fill={isDefaultStroke ? "none" : icon.color}
            xmlns="http://www.w3.org/2000/svg"
            className={className}
            style={{ flexShrink: 0 }}
            aria-hidden="true"
        >
            {icon.paths.map((d, i) => (
                <path
                    key={i}
                    d={d}
                    fill={isDefaultStroke ? "none" : icon.color}
                    stroke={isDefaultStroke ? icon.color : undefined}
                    strokeWidth={isDefaultStroke ? 1.5 : undefined}
                    strokeLinecap={isDefaultStroke ? "round" : undefined}
                    strokeLinejoin={isDefaultStroke ? "round" : undefined}
                />
            ))}
        </svg>
    );
};

export default SetiFileIcon;
