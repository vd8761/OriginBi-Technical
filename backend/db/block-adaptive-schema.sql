-- Create view for adaptive assessment overview
CREATE OR REPLACE VIEW adaptive_assessment_overview AS
SELECT 
    a.assessment_id,
    a.assessment_code,
    a.assessment_name,
    a.module_type,
    a.block_config,
    a.adaptive_config,
    COUNT(ab.block_id) as total_blocks,
    COUNT(CASE WHEN ab.status = 'generated' THEN 1 END) as generated_blocks,
    COUNT(ap.path_id) as available_paths,
    a.status as assessment_status,
    a.created_at
FROM tech_assessments a
LEFT JOIN adaptive_blocks ab ON a.assessment_id = ab.assessment_id
LEFT JOIN adaptive_paths ap ON a.assessment_id = ap.assessment_id AND ap.is_active = true
WHERE (a.block_config->>'enabled')::boolean = true
GROUP BY a.assessment_id, a.assessment_code, a.assessment_name, a.module_type, a.block_config, a.adaptive_config, a.status, a.created_at;
