-- Tech Assessment schema (PostgreSQL)
-- NOTE: This schema assumes an existing users table with users.id.

DO $$
BEGIN
    -- Moved constraint check to after table creation


    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_module_type') THEN
        CREATE TYPE tech_module_type AS ENUM ('aptitude', 'grammar', 'coding', 'mnc', 'role');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_assessment_status') THEN
        CREATE TYPE tech_assessment_status AS ENUM ('draft', 'active', 'closed');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_question_status') THEN
        CREATE TYPE tech_question_status AS ENUM ('active', 'inactive');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_difficulty') THEN
        CREATE TYPE tech_difficulty AS ENUM ('easy', 'medium', 'hard');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_attempt_status') THEN
        CREATE TYPE tech_attempt_status AS ENUM ('in_progress', 'submitted', 'evaluated', 'expired');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_grammar_task_type') THEN
        CREATE TYPE tech_grammar_task_type AS ENUM ('listening_mcq', 'reading_mcq', 'speaking', 'writing');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_compile_status') THEN
        CREATE TYPE tech_compile_status AS ENUM ('success', 'compile_error', 'not_run');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_run_status') THEN
        CREATE TYPE tech_run_status AS ENUM ('passed', 'failed', 'partial', 'not_run');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'tech_role_question_type') THEN
        CREATE TYPE tech_role_question_type AS ENUM ('mcq', 'scenario');
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS tech_assessments (
    assessment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    assessment_code VARCHAR(50) NOT NULL UNIQUE,
    assessment_name VARCHAR(150) NOT NULL,
    module_type tech_module_type NOT NULL,
    total_time_minutes INT NOT NULL,
    total_questions INT NOT NULL,
    shuffle_questions BOOLEAN NOT NULL,
    shuffle_options BOOLEAN NOT NULL,
    negative_mark_enabled BOOLEAN NOT NULL,
    negative_mark_value DECIMAL(5,2) NULL,
    status tech_assessment_status NOT NULL,
    amount DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    trial_question_limit INT NOT NULL DEFAULT 5,
    trial_attempts_limit INT NOT NULL DEFAULT 5,
    main_attempts_limit INT NOT NULL DEFAULT 2,
    created_by BIGINT NOT NULL REFERENCES users(id),
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW()
);

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'chk_tech_assessments_negative_mark') THEN
        ALTER TABLE tech_assessments
            ADD CONSTRAINT chk_tech_assessments_negative_mark
            CHECK ((negative_mark_enabled = FALSE AND negative_mark_value IS NULL)
                OR (negative_mark_enabled = TRUE AND negative_mark_value IS NOT NULL));
    END IF;
END $$;


