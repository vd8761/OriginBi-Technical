    razorpay_order_id VARCHAR(150) NULL,
    razorpay_payment_id VARCHAR(150) NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'active',
    purchased_at TIMESTAMP NOT NULL DEFAULT NOW(),
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW()
);

-- Idempotently relax the constraint on databases that pre-date the change.
DO $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_name = 'tech_assessment_purchases'
          AND column_name = 'assessment_id'
          AND is_nullable = 'NO'
    ) THEN
        EXECUTE 'ALTER TABLE tech_assessment_purchases ALTER COLUMN assessment_id DROP NOT NULL';
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_tech_assessment_purchases_email
    ON tech_assessment_purchases(LOWER(email));

