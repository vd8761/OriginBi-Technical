-- ── 9. Triggers ───────────────────────────────────────────────────────────────
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_question_timing_updated_at') THEN
    CREATE TRIGGER trg_question_timing_updated_at
      BEFORE UPDATE ON adaptive_question_timing FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_adaptive_blueprint_updated_at') THEN
    CREATE TRIGGER trg_adaptive_blueprint_updated_at
      BEFORE UPDATE ON adaptive_blueprint FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_subcategory_coverage_updated_at') THEN
    CREATE TRIGGER trg_subcategory_coverage_updated_at
      BEFORE UPDATE ON adaptive_subcategory_coverage FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
  END IF;
END $$;

