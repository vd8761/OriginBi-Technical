-- ── 3. block_snapshots — immutable, written once on first "Next Block" ────────
-- This is the core of the snapshot-based adaptive model.
-- Once written, it is NEVER updated. Adaptive decisions use this.
CREATE TABLE IF NOT EXISTS block_snapshots (
  snapshot_id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_token          VARCHAR(100) NOT NULL,
  block_number           INT          NOT NULL,
  assessment_id          BIGINT       NOT NULL REFERENCES tech_assessments(assessment_id) ON DELETE CASCADE,
  user_id                BIGINT       NOT NULL REFERENCES users(id),

  -- Snapshot of per-question answers at the moment "Next Block" was first clicked
  -- { questionId: { selectedOptionId, submittedAnswer, isCorrect, marksAwarded, timeTakenSeconds, status } }
  question_answers       JSONB        NOT NULL DEFAULT '{}'::jsonb,

  -- Aggregate metrics computed from snapshot answers
  total_questions        INT          DEFAULT 0,
  correct_count          INT          DEFAULT 0,
  wrong_count            INT          DEFAULT 0,
  skipped_count          INT          DEFAULT 0,
  attempted_count        INT          DEFAULT 0,
  total_block_marks      DECIMAL(8,2) DEFAULT 0,
  obtained_marks         DECIMAL(8,2) DEFAULT 0,
  skipped_marks          DECIMAL(8,2) DEFAULT 0,
  marks_score            DECIMAL(5,2) DEFAULT 0,
  adaptive_accuracy      DECIMAL(5,2) DEFAULT 0,
  attempt_accuracy       DECIMAL(5,2) DEFAULT 0,
  skip_count_rate        DECIMAL(5,2) DEFAULT 0,
  skipped_marks_rate     DECIMAL(5,2) DEFAULT 0,
  skip_impact            DECIMAL(5,2) DEFAULT 0,
  skip_confidence        DECIMAL(5,2) DEFAULT 0,
  difficulty_handling    DECIMAL(5,2) DEFAULT 0,
  speed_efficiency       DECIMAL(5,2) DEFAULT 0,
  block_readiness_score  DECIMAL(5,2) DEFAULT 0,
  next_block_difficulty  VARCHAR(10)  DEFAULT 'easy'
    CHECK (next_block_difficulty IN ('easy','medium','hard')),
  time_taken_seconds     INT          DEFAULT 0,

  -- Blueprint slot coverage for this block (which subcategories were used)
  coverage_map           JSONB        DEFAULT '{}'::jsonb,

  created_at             TIMESTAMP    NOT NULL DEFAULT NOW(),

  UNIQUE (attempt_token, block_number)
);

-- ── 4. adaptive_question_timing — per-question time tracking ─────────────────
-- Stores time spent on each question within a block attempt.
CREATE TABLE IF NOT EXISTS adaptive_question_timing (
  timing_id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_token          VARCHAR(100) NOT NULL,
  block_number           INT          NOT NULL,
  question_id            BIGINT       NOT NULL,
  time_started           TIMESTAMP    NULL,
  time_ended             TIMESTAMP    NULL,
  time_taken_seconds     INT          DEFAULT 0,
  expected_time_seconds  INT          DEFAULT 0,
  speed_score            DECIMAL(5,2) DEFAULT 0,
  review_count           INT          DEFAULT 0,   -- how many times user revisited
  created_at             TIMESTAMP    NOT NULL DEFAULT NOW(),
  updated_at             TIMESTAMP    NOT NULL DEFAULT NOW(),
  UNIQUE (attempt_token, block_number, question_id)
);

-- ── 5. adaptive_blueprint — marks blueprint per assessment ───────────────────
-- Stores the marks blueprint created when admin sets up an adaptive assessment.
CREATE TABLE IF NOT EXISTS adaptive_blueprint (
  blueprint_id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assessment_id          BIGINT       NOT NULL REFERENCES tech_assessments(assessment_id) ON DELETE CASCADE,
  total_marks            DECIMAL(8,2) NOT NULL DEFAULT 100,
  total_blocks           INT          NOT NULL DEFAULT 4,
  marks_per_block        DECIMAL(8,2) NOT NULL DEFAULT 25,
  seconds_per_mark       INT          NOT NULL DEFAULT 45,

  -- Category weightage: { "Quantitative Aptitude": { weightPct: 30, targetMarks: 30 }, ... }
  category_blueprint     JSONB        NOT NULL DEFAULT '{}'::jsonb,

  -- Subcategory blueprint: { "Quantitative Aptitude": { "Number System": { targetMarks: 5 }, ... } }
  subcategory_blueprint  JSONB        NOT NULL DEFAULT '{}'::jsonb,

  -- Difficulty profiles: { easy: {easy:70,medium:30,hard:0}, medium: {...}, hard: {...} }
  difficulty_profiles    JSONB        NOT NULL DEFAULT '{
    "easy":   {"easy":70,"medium":30,"hard":0},
    "medium": {"easy":20,"medium":60,"hard":20},
    "hard":   {"easy":0,"medium":30,"hard":70}
  }'::jsonb,

  created_at             TIMESTAMP    NOT NULL DEFAULT NOW(),
  updated_at             TIMESTAMP    NOT NULL DEFAULT NOW(),
  UNIQUE (assessment_id)
);

-- ── 6. adaptive_subcategory_coverage — tracks subcategory rotation ───────────
-- Updated after each block is generated so the next block can rotate topics.
CREATE TABLE IF NOT EXISTS adaptive_subcategory_coverage (
  coverage_id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_token          VARCHAR(100) NOT NULL,
  assessment_id          BIGINT       NOT NULL REFERENCES tech_assessments(assessment_id) ON DELETE CASCADE,
  -- { "Quantitative Aptitude": { "Number System": { marksUsed: 5, questionsUsed: 2 }, ... } }
  coverage               JSONB        NOT NULL DEFAULT '{}'::jsonb,
  created_at             TIMESTAMP    NOT NULL DEFAULT NOW(),
  updated_at             TIMESTAMP    NOT NULL DEFAULT NOW(),
  UNIQUE (attempt_token)
);

