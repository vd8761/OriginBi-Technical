-- 6. Advanced caching with TTL
CREATE TABLE adaptive_cache_v2 (
    cache_key VARCHAR(255) PRIMARY KEY,
    cache_data JSONB NOT NULL,
    cache_type VARCHAR(50) NOT NULL, -- 'block_questions', 'difficulty_calc', 'user_profile'
    assessment_id BIGINT,
    user_id BIGINT,
    
    -- TTL management
    expires_at TIMESTAMP NOT NULL,
    hit_count INT DEFAULT 0,
    last_accessed TIMESTAMP NOT NULL DEFAULT NOW(),
    
    -- Cache metadata
    data_size_bytes INT DEFAULT 0,
    generation_time_ms INT DEFAULT 0,
    cache_version INT DEFAULT 1,
    
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW()
);

-- 7. Machine learning model storage (for future AI adaptation)
CREATE TABLE adaptive_ml_models (
    model_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    model_name VARCHAR(100) NOT NULL UNIQUE,
    model_type VARCHAR(50) NOT NULL, -- 'difficulty_prediction', 'path_optimization', 'time_estimation'
    model_version VARCHAR(20) NOT NULL,
    
    -- Model storage
    model_data JSONB NOT NULL,
    feature_importance JSONB DEFAULT '{}'::jsonb,
    performance_metrics JSONB DEFAULT '{}'::jsonb,
    
    -- Training data
    training_data_points INT DEFAULT 0,
    last_trained_at TIMESTAMP,
    model_accuracy DECIMAL(5,4) DEFAULT 0.0000,
    
    -- Deployment status
    is_active BOOLEAN NOT NULL DEFAULT FALSE,
    deployed_at TIMESTAMP,
    a_b_test_group VARCHAR(20), -- 'control', 'variant_a', 'variant_b'
    
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW()
);

-- 8. User adaptive profiles (personalization)
CREATE TABLE adaptive_user_profiles (
    profile_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id) UNIQUE,
    
    -- Learning characteristics
    learning_pace VARCHAR(20) DEFAULT 'moderate', -- 'slow', 'moderate', 'fast'
    difficulty_preference VARCHAR(10) DEFAULT 'adaptive', -- 'easy', 'medium', 'hard', 'adaptive'
    time_pressure_tolerance DECIMAL(5,4) DEFAULT 0.5000, -- 0-1 scale
    
    -- Performance history
    average_accuracy DECIMAL(5,4) DEFAULT 0.0000,
    preferred_categories JSONB DEFAULT '[]'::jsonb,
    weak_categories JSONB DEFAULT '[]'::jsonb,
    
    -- Behavioral patterns
    peak_performance_hours JSONB DEFAULT '[9,10,11,14,15,16]'::jsonb,
    session_length_preference INT DEFAULT 30, -- minutes
    break_frequency INT DEFAULT 3, -- questions between breaks
    
    -- Adaptive settings
    auto_difficulty_adjustment BOOLEAN DEFAULT TRUE,
    personalized_paths BOOLEAN DEFAULT TRUE,
    time_adaptation_enabled BOOLEAN DEFAULT TRUE,
    
    -- Profile analytics
    total_assessments_taken INT DEFAULT 0,
    total_time_spent INT DEFAULT 0, -- seconds
    improvement_rate DECIMAL(5,4) DEFAULT 0.0000,
    
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW()
);

-- Enhanced indexes for performance
CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_block_questions_block_seq 
ON adaptive_block_questions (block_id, sequence_order);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_question_attempts_block_question 
ON adaptive_question_attempts (block_attempt_id, question_id);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_analytics_date_user 
ON adaptive_analytics_partitioned (analytics_date, user_id);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_cache_expires_type 
ON adaptive_cache_v2 (expires_at, cache_type);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_user_profiles_learning 
ON adaptive_user_profiles (learning_pace, difficulty_preference);

-- Advanced triggers for automated analytics
CREATE OR REPLACE FUNCTION update_analytics_trigger()
RETURNS TRIGGER AS $$
BEGIN
    -- Update user profile when block attempt completes
    IF NEW.status = 'completed' AND OLD.status != 'completed' THEN
        UPDATE adaptive_user_profiles 
        SET 
            total_assessments_taken = total_assessments_taken + 1,
            total_time_spent = total_time_spent + NEW.total_time_seconds,
            average_accuracy = (
                SELECT AVG(accuracy_rate) 
                FROM enhanced_block_attempts 
                WHERE user_id = NEW.user_id 
                AND status = 'completed'
            ),
            updated_at = NOW()
        WHERE user_id = NEW.user_id;
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_update_analytics
    AFTER UPDATE ON enhanced_block_attempts
    FOR EACH ROW
    EXECUTE FUNCTION update_analytics_trigger();

-- Automated cache cleanup
CREATE OR REPLACE FUNCTION cleanup_expired_cache()
RETURNS void AS $$
BEGIN
    DELETE FROM adaptive_cache_v2 
    WHERE expires_at < NOW() - INTERVAL '1 hour';
    
    DELETE FROM adaptive_cache_v2 
    WHERE last_accessed < NOW() - INTERVAL '24 hours' 
    AND hit_count < 5;
END;
$$ LANGUAGE plpgsql;

