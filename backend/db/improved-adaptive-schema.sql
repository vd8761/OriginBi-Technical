-- IMPROVED ADAPTIVE SCHEMA - More Advanced Approach

-- 1. Normalized difficulty distributions
CREATE TABLE adaptive_difficulty_distributions (
    distribution_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    block_number INT NOT NULL,
    easy_weight DECIMAL(5,2) NOT NULL DEFAULT 33.33,
    medium_weight DECIMAL(5,2) NOT NULL DEFAULT 33.33,
    hard_weight DECIMAL(5,2) NOT NULL DEFAULT 33.34,
    total_weight DECIMAL(5,2) GENERATED ALWAYS AS (
        easy_weight + medium_weight + hard_weight
    ) STORED,
    created_at TIMESTAMP NOT NULL DEFAULT NOW()
);

-- 2. Separate question mapping table (better than JSON array)
CREATE TABLE adaptive_block_questions (
    block_question_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    block_id BIGINT NOT NULL REFERENCES adaptive_blocks(block_id) ON DELETE CASCADE,
    question_id BIGINT NOT NULL, -- Reference to actual question table
    question_type VARCHAR(20) NOT NULL, -- 'aptitude', 'grammar', 'mnc', etc.
    sequence_order INT NOT NULL,
    difficulty VARCHAR(10) NOT NULL CHECK (difficulty IN ('easy', 'medium', 'hard')),
    category VARCHAR(100) NOT NULL,
    points DECIMAL(5,2) NOT NULL DEFAULT 1.0,
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE (block_id, question_id, question_type)
);

-- 3. Enhanced block attempts with more granular tracking
CREATE TABLE enhanced_block_attempts (
    attempt_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    attempt_token VARCHAR(100) NOT NULL,
    block_id BIGINT NOT NULL REFERENCES adaptive_blocks(block_id),
    user_id BIGINT NOT NULL REFERENCES users(id),
    block_number INT NOT NULL,
    
    -- Timing analytics
    started_at TIMESTAMP NOT NULL DEFAULT NOW(),
    first_question_at TIMESTAMP,
    last_question_at TIMESTAMP,
    completed_at TIMESTAMP,
    total_time_seconds INT GENERATED ALWAYS AS (
        EXTRACT(EPOCH FROM (completed_at - started_at))
    ) STORED,
    
    -- Performance metrics
    questions_attempted INT NOT NULL DEFAULT 0,
    questions_correct INT NOT NULL DEFAULT 0,
    accuracy_rate DECIMAL(5,4) GENERATED ALWAYS AS (
        CASE 
            WHEN questions_attempted > 0 
            THEN (questions_correct::DECIMAL / questions_attempted::DECIMAL)
            ELSE 0 
        END
    ) STORED,
    
    -- Adaptive metrics
    difficulty_achieved VARCHAR(10) NOT NULL,
    adaptation_score DECIMAL(5,4) NOT NULL DEFAULT 0.0000,
    next_difficulty_recommended VARCHAR(10),
    
    -- Status tracking
    status VARCHAR(20) NOT NULL DEFAULT 'in_progress' 
        CHECK (status IN ('in_progress', 'completed', 'abandoned', 'timeout')),
    
    -- Behavioral analytics
    tab_switches INT DEFAULT 0,
    copy_attempts INT DEFAULT 0,
    idle_time_seconds INT DEFAULT 0,
    
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE (attempt_token, block_number)
);

-- 4. Question-level attempt tracking (for detailed analytics)
CREATE TABLE adaptive_question_attempts (
    question_attempt_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    block_attempt_id BIGINT NOT NULL REFERENCES enhanced_block_attempts(attempt_id) ON DELETE CASCADE,
    question_id BIGINT NOT NULL,
    question_type VARCHAR(20) NOT NULL,
    sequence_order INT NOT NULL,
    
    -- Answer tracking
    selected_option_id VARCHAR(50),
    is_correct BOOLEAN NOT NULL DEFAULT FALSE,
    points_earned DECIMAL(5,2) NOT NULL DEFAULT 0.0,
    time_taken_seconds INT NOT NULL DEFAULT 0,
    
    -- Behavioral data
    attempts_count INT NOT NULL DEFAULT 1,
    first_viewed_at TIMESTAMP NOT NULL DEFAULT NOW(),
    answered_at TIMESTAMP,
    review_count INT DEFAULT 0,
    
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE (block_attempt_id, question_id, question_type)
);

-- 5. Real-time adaptive analytics (partitioned for performance)
CREATE TABLE adaptive_analytics_partitioned (
    analytics_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    assessment_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    attempt_token VARCHAR(100) NOT NULL,
    
    -- Partition key by date for performance
    analytics_date DATE NOT NULL DEFAULT CURRENT_DATE,
    
    -- Performance summary
    final_score DECIMAL(8,2) NOT NULL DEFAULT 0,
    max_possible_score DECIMAL(8,2) NOT NULL DEFAULT 0,
    score_percentage DECIMAL(5,4) GENERATED ALWAYS AS (
        CASE 
            WHEN max_possible_score > 0 
            THEN (final_score / max_possible_score) * 100
            ELSE 0 
        END
    ) STORED,
    
    completion_time_seconds INT NOT NULL DEFAULT 0,
    average_time_per_question DECIMAL(8,2) GENERATED ALWAYS AS (
        CASE 
            WHEN (SELECT COUNT(*) FROM adaptive_question_attempts qa 
                  JOIN enhanced_block_attempts ba ON qa.block_attempt_id = ba.attempt_id 
                  WHERE ba.attempt_token = adaptive_analytics_partitioned.attempt_token) > 0
            THEN completion_time_seconds::DECIMAL / 
                 (SELECT COUNT(*) FROM adaptive_question_attempts qa 
                  JOIN enhanced_block_attempts ba ON qa.block_attempt_id = ba.attempt_id 
                  WHERE ba.attempt_token = adaptive_analytics_partitioned.attempt_token)
            ELSE 0 
        END
    ) STORED,
    
    -- Adaptive effectiveness
    adaptation_success_score DECIMAL(5,4) NOT NULL DEFAULT 0.0000,
    difficulty_progression_smoothness DECIMAL(5,4) DEFAULT 0.0000,
    prediction_accuracy DECIMAL(5,4) DEFAULT 0.0000,
    
    -- Learning analytics
    weak_areas JSONB DEFAULT '[]'::jsonb,
    strong_areas JSONB DEFAULT '[]'::jsonb,
    improvement_suggestions JSONB DEFAULT '[]'::jsonb,
    
    -- System metrics
    system_performance_score DECIMAL(5,4) DEFAULT 0.0000,
    user_satisfaction_score DECIMAL(5,4) DEFAULT 0.0000,
    
    created_at TIMESTAMP NOT NULL DEFAULT NOW()
) PARTITION BY RANGE (analytics_date);

