-- ── 8. updated_at triggers ────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN NEW.updated_at = NOW(); RETURN NEW; END;
$$ LANGUAGE plpgsql;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_adaptive_blocks_updated_at') THEN
    CREATE TRIGGER trg_adaptive_blocks_updated_at
      BEFORE UPDATE ON adaptive_blocks FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_block_attempts_updated_at') THEN
    CREATE TRIGGER trg_block_attempts_updated_at
      BEFORE UPDATE ON block_attempts FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_adaptive_paths_updated_at') THEN
    CREATE TRIGGER trg_adaptive_paths_updated_at
      BEFORE UPDATE ON adaptive_paths FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
  END IF;
END $$;

